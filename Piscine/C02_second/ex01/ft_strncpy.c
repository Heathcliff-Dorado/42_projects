char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (src[i] != '\0' && n > 0)
	{
		dest[i] = src[i];
		i++;
		n--;
	}
	if (n > 0)
	{
		while (n > 0)
		{
			dest[i] = 0;
			i++;
			n--;
		}
	}
	return (dest);
}
